package com.boanerges.poc.exception;

/**
 * @author jfahiba
 *
 */
public class CardinalNotFoundException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6720332279475337727L;

	public CardinalNotFoundException() {
		super();
	}

	public CardinalNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public CardinalNotFoundException(String message) {
		super(message);
	}

	public CardinalNotFoundException(Throwable cause) {
		super(cause);
	}

	

}
