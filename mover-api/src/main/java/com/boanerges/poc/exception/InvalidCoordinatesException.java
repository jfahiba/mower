package com.boanerges.poc.exception;

/**
 * @author jfahiba
 *
 */
public class InvalidCoordinatesException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4556428466336691594L;

	public InvalidCoordinatesException() {
		super();
	}

	public InvalidCoordinatesException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidCoordinatesException(String message) {
		super(message);
	}

	public InvalidCoordinatesException(Throwable cause) {
		super(cause);
	}

	
}
